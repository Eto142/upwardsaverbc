<script>
  // Update slider values
  document.getElementById('loanAmount').addEventListener('input', function() {
    document.getElementById('loanAmountValue').textContent = '$' + this.value.toLocaleString();
  });
  
  document.getElementById('interestRate').addEventListener('input', function() {
    document.getElementById('interestRateValue').textContent = this.value + '%';
  });
  
  document.getElementById('loanTerm').addEventListener('input', function() {
    document.getElementById('loanTermValue').textContent = this.value;
  });

  // Loan calculation function
  function calculateLoan() {
    const amount = parseFloat(document.getElementById('loanAmount').value);
    const interest = parseFloat(document.getElementById('interestRate').value) / 100 / 12;
    const payments = parseFloat(document.getElementById('loanTerm').value);
    
    // Calculate monthly payment
    const x = Math.pow(1 + interest, payments);
    const monthly = (amount * x * interest) / (x - 1);
    const monthlyPayment = monthly.toFixed(2);
    const totalInterest = (monthly * payments - amount).toFixed(2);
    
    // Display results
    document.getElementById('monthlyPayment').textContent = '$' + monthlyPayment;
    document.getElementById('totalInterest').textContent = '$' + totalInterest;
    document.getElementById('calculatorResult').style.display = 'block';
  }
</script>